<h2>Technology<span style="font-size:15px;">(and method) </span>to end hunger:</h2>
We introduce a machine the size of a 40 foot shipping container that produces food everyday.

<h3>What each machine does:</h3>
Every hour, from 9:00h to 15:00h, up to 7 hot baked sweet potatoes are served by each machine, totaling up to 40 every day.<br>
The sweet potato is the food that produces the most carbohydrates per square meter.<br>

É uma estufa pequena tecnológica que será localizada em locais de venda direta ao consumidor como as rolotes de farturas
<h3>About the machines:</h3>
<div style="float:left; "><img style="width:140px; height:auto;" src="estrutura.png"/></div>
<div style="float:none; ">
The machines are fundamentally high performance greenhouses, having 30 square meters and 4 floors, making a total of 120 square meters of culture, growing sweet potatoes. <b>Each machine has a robot inside</b> that waters, plants and harvests the sweet potatoes in precise time. The robot harvests a row of ten plants each day, planting new ones on the same local.<br>
The robot takes the harvested sweet potatoes to the oven placed on the front of the machine and roast them during the day, starting at 9:00h, then at each hour, the robot take the roasted sweet potatoes and put another set of up to 7 on the oven, 6 times, 6 hours baking sweet potatoes, baking all of the harvested sweet potatoes of the day until about 15:00h.<br>
The power comes from solar panels and from a wind turbine wich are placed on the roof of the container, after the container is parked.
</div>

<h3>Method to end hunger:</h3>
The machines work well if they are spread out as close to the tribes as possible, they are left where a semi-trailer can go.<br>
Each machine is transported only once to the site, which then produces minimally enough food for 20 people every day.<br>
Each machine is good for places with climatic changes since it can withstand up to 8 months of drought.<br>
